function getDemoResponse(query: string): string {
  const q = query.toLowerCase();
  if ((q.includes('tp53') || q.includes('p53')) && (q.includes('brca1') || q.includes('brca'))) {
    return DEMO_RESPONSES.tp53_brca1;
  }
  if ((q.includes('egfr')) && (q.includes('kras'))) {
    return DEMO_RESPONSES.egfr_kras;
  }
  return DEMO_RESPONSES.default;
}

// ---------- Groq -----------------------------------------------------------

async function queryGroq(prompt: string): Promise<string> {
  const apiKey = import.meta.env.VITE_GROQ_API_KEY as string | undefined;
  if (!apiKey) throw new Error('No Groq key');
  const client = new Groq({ apiKey, dangerouslyAllowBrowser: true });
  const completion = await client.chat.completions.create({
    model: 'llama-3.3-70b-versatile',
    messages: [
      {
        role: 'system',
        content:
          'You are OncoCorr, an expert oncology & multi-omics research assistant. ' +
          'Provide concise, evidence-based answers about genes, cancer biology, ' +
          'biomarkers, and clinical correlations. Use markdown formatting.',
      },
      { role: 'user', content: prompt },
    ],
    max_tokens: 1024,
    temperature: 0.4,
  });
  const text = completion.choices[0]?.message?.content;
  if (!text) throw new Error('Empty Groq response');
  return text;
}

// ---------- Gemini ---------------------------------------------------------

async function queryGemini(prompt: string): Promise<string> {
  const apiKey = import.meta.env.VITE_GEMINI_API_KEY as string | undefined;
  if (!apiKey) throw new Error('No Gemini key');
  const url =
    `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${apiKey}`;
  const body = {
    contents: [
      {
        parts: [
          {
            text:
              'You are OncoCorr, an expert oncology & multi-omics research assistant. ' +
              'Provide concise, evidence-based answers. Use markdown formatting.\n\n' +
              prompt,
          },
        ],
      },
    ],
    generationConfig: { maxOutputTokens: 1024, temperature: 0.4 },
  };
  const res = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });
  if (!res.ok) throw new Error(`Gemini HTTP ${res.status}`);
  const data = await res.json();
  const text = data?.candidates?.[0]?.content?.parts?.[0]?.text;
  if (!text) throw new Error('Empty Gemini response');
  return text;
}

// ---------- Ollama ---------------------------------------------------------

async function queryOllama(prompt: string): Promise<string> {
  const base = import.meta.env.VITE_OLLAMA_URL || 'http://localhost:11434';
  const model = import.meta.env.VITE_OLLAMA_MODEL || 'llama3.2';
  const res = await fetch(`${base}/api/generate`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      model,
      prompt:
        'You are OncoCorr, an expert oncology & multi-omics research assistant. ' +
        'Provide concise, evidence-based answers. Use markdown formatting.\n\n' +
        prompt,
      stream: false,
    }),
  });
  if (!res.ok) throw new Error(`Ollama HTTP ${res.status}`);
  const data = await res.json();
  if (!data?.response) throw new Error('Empty Ollama response');
  return data.response as string;
}

// ---------- Public API -----------------------------------------------------

export async function askAI(prompt: string): Promise<AIResponse> {
  // 1. Groq
  if (import.meta.env.VITE_GROQ_API_KEY) {
    try {
      return { text: await queryGroq(prompt), provider: 'groq' };
    } catch (e) {
      console.warn('Groq failed, trying next provider:', e);
    }
  }
  // 2. Gemini
  if (import.meta.env.VITE_GEMINI_API_KEY) {
    try {
      return { text: await queryGemini(prompt), provider: 'gemini' };
    } catch (e) {
      console.warn('Gemini failed, trying next provider:', e);
    }
  }
  // 3. Ollama (local)
  try {
    return { text: await queryOllama(prompt), provider: 'ollama' };
  } catch {
    // Ollama not running locally — fall through to demo
  }
  // 4. Demo / mock
  return { text: getDemoResponse(prompt), provider: 'demo' };
}

export function activeProvider(): AIProvider {
  if (import.meta.env.VITE_GROQ_API_KEY) return 'groq';
  if (import.meta.env.VITE_GEMINI_API_KEY) return 'gemini';
  return 'demo'; // Ollama detection is async; shown after first call
}
